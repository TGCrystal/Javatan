package edu.rpi.cs.csci4960.s21.javatan;

/**
* Indicates the player color of something, either no color, red, orange, white, or blue
*
* @author Chuanfeng Xiong
* @author Chris Lamberston
* @author Ruben McWilliams
* @author Trevor Crystal
*/
public enum PlayerColor {
    /**
    * Represents no player
    */
    NONE,
    /**
    * Represents the player with red pieces
    */
    RED,
    /**
    * Represents the player with orange pieces
    */
    ORANGE,
    /**
    * Represents the player with white pieces
    */
    WHITE,
    /**
    * Represents the player with blue pieces
    */
    BLUE
};